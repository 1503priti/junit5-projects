package com.priti;

public class Student {
    public int is;
    public String name;

    public Student(int is, String name) {
        this.is = is;
        this.name = name;
    }

    public int getIs() {
        return is;
    }

    public void setIs(int is) {
        this.is = is;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
